let contractAddress = "0x40089B4198f6adCe8F713A501cFb627a38974f79";
let provider = ethers.getDefaultProvider("goerli");


const loadWallet = (privateKey) => {
    if (!etherWallet) {
        etherWallet = new ethers.Wallet(privateKey, provider)
    }

    return etherWallet
}

const getContract = () => {
    if (!etherWallet) {
        console.error("load wallet first!")
        return false
    }

    let contract = new ethers.Contract(contractAddress, abi, provider);


    let contractWithSigner = contract.connect(etherWallet);
    console.log('contract WTF', contract, etherWallet, contractWithSigner)
    return contractWithSigner
}

export const getWalletAndContract = (privateKey) => {
    const wallet = loadWallet(privateKey)
    const contract = getContract()

    console.log('get wallet and contract', wallet, contract)
    return { wallet, contract}
}

export const decryptWallet = async (json, password) => {
    let wallet = null

    try {
        wallet = await ethers.Wallet.fromEncryptedJson(json, password)
        wallet = wallet.connect(provider)

        etherWallet = wallet
    } catch (e) {
        throw e
    }

    return wallet
}
